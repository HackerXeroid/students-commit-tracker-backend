const mongoose = require("mongoose");

const userSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    unique: true,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
  role: {
    type: String,
    enum: ["student", "teacher", "teacher_pending", "teacher_rejected"],
    default: "student",
  },
});

const User = mongoose.model("User", userSchema);

module.exports = User;
